  "    var desc=document.createElement('div'); desc.className='hint'; desc.appendChild(text(sb.description||''));",
  "    var meta=document.createElement('div'); meta.className='hint'; meta.appendChild(text((sb.createdAt?new Date(sb.createdAt).toLocaleString():'')+' • '+(sb.itemCount||0)+' item(s)'));",
