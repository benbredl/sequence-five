  "  }",

  "  g('create').addEventListener('click', async function(){",
  "    var btn=this; var title=g('title').value.trim(); var desc=g('desc').value.trim();",
  "    if(!title){ alert('Please enter a title'); return; }",
  "    btn.disabled=true; btn.textContent='Creating…';",
  "    try{",
  "      var r=await fetch('/api/storyboards',{method:'POST',headers:{'Content-Type':'application/json'},body:JSON.stringify({title:title,description:desc})});",
