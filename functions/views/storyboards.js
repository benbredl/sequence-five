  "    var right=document.createElement('div'); right.className='row';",
  "    var open=document.createElement('a'); open.className='pill'; open.href='/storyboard?id='+(sb.id||''); open.appendChild(text('Open ▶'));",
  "    right.appendChild(open);",
  "    row.appendChild(left); row.appendChild(right); wrap.appendChild(row);",
  "    return wrap;",
  "  }",

  "  async function load(){",
  "    var list=g('list'); list.innerHTML='<div class=\"hint\"><span class=\"spinner\"></span> Loading…</div>';",
